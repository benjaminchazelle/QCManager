function QuestionSearchController() {
	
	if(questionsFrame == null) return;
	
	var searchterm = littleCasify(document.getElementById("searchterm").value);
	
	var questions = questionsFrame.document.getElementById("questions_framed").children;
	
	for(var i=0;i<questions.length;i++) {

		questions.item(i).style.display = (questions.item(i).getAttribute("name").indexOf(searchterm) == -1) ? "none" : "";
	
	}
	
}

